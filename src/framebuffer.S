.equ ramarea, 0x02000000
.equ CursorX,ramarea+32
.equ CursorY,ramarea+33

.text
.global PrintString
PrintString:
  STMFD sp!,{r0-r12, lr}
PrintStringAgain:
  ldrB r0,[r1],#1
  cmp r0,#0
  beq PrintStringDone
  bl PrintChar
  b PrintStringAgain
PrintStringDone:
  LDMFD sp!,{r0-r12, pc}

.global PrintChar
PrintChar:
  STMFD sp!,{r0-r12, lr}
  mov r4,#0
  mov r5,#0

  ldr r3,=#CursorX
  ldrB r4,[r3]              // X pos
  ldr r3,=#CursorY
  ldrB r5,[r3]              // Y pos

  mov r3,#0x06000000        // VRAM base

  mov r6,#8*2               // Xpos, 2 bytes per pixel, 8 bytes per char
  mul r2,r4,r6
  add r3,r3,r2

  mov r4,#240*8*2           // Ypos, 240 pixels per line
  mul r2,r5,r4              // 2 bytes per pixel, 8 lines per char
  add r3,r3,r2

  ldr r4,=BitmapFont         // Font source
  sub r0,r0,#32             // First Char is 32 (space)
  add r4,r4,r0,asl #3       // 8 bytes per char

  mov r1,#8                 // 8 lines
DrawLine:
  mov r7,#8                 // 8 pixels per line
  ldrb r8,[r4],#1           // Load Letter
  mov r9,#0b100000000       // Mask

  ldr r2, =0b1111111101000000 // Color: ABBBBBGGGGGRRRRR   A=Alpha
DrawPixel:
  tst r8,r9                 // Is bit 1?
  strneh r2,[r3]            // Yes? then fill pixel (HalfWord)
  add r3,r3,#2
  mov r9,r9,ror #1          // Bitshift Mask
  subs r7,r7,#1
  bne DrawPixel             // Next Hpixel

  add r3,r3,#480-16         // Move Down a line (240 pixels *2 bytes)
  subs r1,r1,#1             // -1 char (16 px)
  bne DrawLine              // Next Vline
LineDone:
  ldr r3,=#CursorX
  ldrB r0,[r3]
  add r0,r0,#1              // Move across screen
  strB r0,[r3]
  LDMFD sp!,{r0-r12, pc}

